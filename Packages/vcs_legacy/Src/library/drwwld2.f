      SUBROUTINE DRWWLD2(LUN,X1,X2,Y1,Y2)
CC    SUBROUTINE DRWWLD2(LUN,X1,X2,Y1,Y2,XS1,XS2,YS1,YS2)
CC
CC
CC  THIS ROUTINE DRAWS
CC  1) ACTUAL MODERN WORLD CONTINENTAL OUTLINES (..B1.DAT)
CC  2) US STATE OUTLINES (..B2.DAT)
CC
CC  INPUT:      LUN = TEMPORARY LUN TO USE FOR THE OUTLINE DATA FILE.
CC          [X1,X2] = LONGITUDINAL DOMAIN WITHIN WHICH TO DRAW.
CC          [Y1,Y1] = LATITUDINAL DOMAIN WITHIN WHICH TO DRAW.
CC        [XS1,XS2] = NORMALIZED LONGITUDINAL DOMAIN WITHIN WHICH TO DRAW.
CC        [YS1,YS2] = NORMALIZED LATITUDINAL DOMAIN WITHIN WHICH TO DRAW.
CC             FNAM = THE DATA FILE WITH THE X,Y LOCATIONS
CC
      DIMENSION XLAT(100),XLON(100)
      REAL*4 X(200),Y(200),XN(200),YN(200)
      INTEGER*2 NPTS

C
C VARIABLE DEFINITIONS
C     NPTS = TOTAL NUMBER OF POINTS IN NEXT LINE (NPTS/2 LAT-LON PAIRS).
C            IF NPTS < 2 THIS RECORD IS A GROUP SEPARATOR AND NO POINT
C            PAIRS FOLLOW.
C     IGID = GROUP ID FOR THESE POINTS WHERE
C             1 = CONTINENTAL OUTLINES
C             2 = US STATE BOUNDARIES (HIGHER RESOLUTION THAN 1)
C             3 = INTERNATIONAL POLITICAL BOUNDARIES
C
C     XLATMX,XLATMN,XLONMX,XLONMN  = THE LAT-LON LIMITS FOR POINTS IN
C               THE FOLLOWING LINE.
C
C     XLAT,XLON ARE THE COORDINATES OF A CONTINUOUS LINE IN THE OUTLINE
C       CONTAINING NPTS/2 POINTS.
C
C TAPE STRUCTURE
C     THE TAPE IS DIVIDED INTO FOUR GROUPS, SEPARATED BY LINE HEADERS
C     WITH POINT COUNTS LESS THAN 2.
C     GROUP     CONTENT
C      1        CONTINENTAL OUTLINES ONLY.
C      2        US STATE OULINES ONLY (HIGHER RESOLUTION THAN 1).
C      3        CONTINENTAL AND US STATE AND INTERNATIONAL POLITICAL
C               BOUNDARIES (USES 2 WHERE IT OVERLAPS 1).
C      4        CONTINENTAL AND INTERNATIONAL POLITICAL BOUNDARIES.
      NGRP=0
C
CC      OPEN(1,FILE='/data1/vcs_legacy_release2_5/vcs_legacy_script/data',STATUS='OLD')
      OPEN (UNIT=LUN,FILE='/data1/vcs_legacy_release2_5/vcs_legacy_script/data',
     *      STATUS='OLD',FORM='UNFORMATTED')
    5 NLINE=0
      NGRP=NGRP+1
C READ LINE HEADER
   10 READ (1,1001,END=90)NPTS,IGID,XLATMX,XLATMN,XLONMX,XLONMN
 1001 FORMAT(2I8,4F8.3)
      IF(NPTS.LT.2) GO TO 90
      NPR=(NPTS+1)/2
C READ LINE
      READ(1,1002,END=90)(XLAT(I),XLON(I),I=1,NPR)
 1002 FORMAT(10F8.3)
      NLINE=NLINE+1
      IF(NLINE.GT.10) GO TO 10
      PRINT 1003,NPTS,IGID,XLATMX,XLATMN,XLONMX,XLONMN,XLAT(1),
     2 XLON(1),XLAT(NPR),XLON(NPR)
 1003 FORMAT(' LINE ',I5,I3,8F9.3)
      GO TO 10
   90 CONTINUE
      PRINT 1004,NGRP,NLINE
 1004 FORMAT(' END OF GROUP ',I2,' LINES =',I6)
      IF(NLINE.GT.0) GO TO 5
      END
